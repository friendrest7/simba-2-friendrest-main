export const translate = (lang: Lang, key: string) =>
  dictionaries[lang][key] ?? dictionaries.en[key] ?? key;

type I18nCtx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: keyof typeof en | string) => string;
};

const Ctx = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    const saved = (typeof window !== "undefined" && localStorage.getItem("simba.lang")) as Lang | null;
    if (saved && dictionaries[saved]) {
      setLangState(saved);
    }
  }, []);

  const setLang = (nextLang: Lang) => {
    setLangState(nextLang);
    if (typeof window !== "undefined") {
      localStorage.setItem("simba.lang", nextLang);
    }
  };

  const t = (key: string) => translate(lang, key);

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export const useI18n = () => {
  const context = useContext(Ctx);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
};
